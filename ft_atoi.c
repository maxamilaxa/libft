#include "libft.h"

int		ft_atoi(char *str)
{
	int	res;
	int m;

	res = 0;
	m = 1;
	while (*str && (*str <= 47))
		str++;
	if ((*str == '-') || (*str == '+'))
	{
		if (*str == '-')
			m = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = (res * 10) + (*str - '0');
		str++;
	}
	return (res * m);
}
